							<a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">Mascotas en casa: Consejos para mantener la casa limpia</a>
							<p class="card-text">Es importante que nuestra mascota tenga un lugar propio donde dormir que pueda limpiarse con facilidad. Si es una cama, debe ser de materiales lavables y que no acumulen mucha suciedad fácilmente. Si tiene una caseta, la limpieza es más fácil y rápida, pues los materiales de los que están hechas están pensados para ello
                            <br>
                            <br>

                            También debe tener un lugar donde comer y beber que, preferiblemente, no debemos ubicar en la cocina, pues pueden dejar restos como pelos o babas que, unidos a otros restos orgánicos, produzcan contaminación.

                            Debemos bañar con asiduidad a nuestra mascota con productos específicos para ella. Los gatos deben bañarse más ocasionalmente que los perros. Ten cuidado de no pasarte con los baños, pues pueden eliminar la capa protectora natural del pelaje de tu mascota. Consulta a tu veterinario sobre la periodicidad adecuada
                            <br>
                            <br>
                           Oroles: Los perros tienen un olor característico que se debe a los procesos químicos de su organismo.

                            Para minimizar estos olores podemos hacer dos cosas: rociar a nuestra mascota con una mezcla preparada con una taza de vinagre de manzana y un litro de agua, y luego cepillar; o espolvorear bicarbonato por encima del pelo del animal y luego cepillar.

                            Otro de los olores característicos de nuestras mascotas es su aliento. En tiendas de animales y clínicas veterinarias tenemos toda clase de productos para limpiar y refrescar la boca de nuestros animales y evitar así el mal aliento.</p>
                            <br>
                            <br>
                            <p>Pelos:Cepilla a tu mascota periódicamente, teniendo especial atención si es de pelo largo. Procura hacerlo en lugares abiertos y aislados del resto de la casa.

                            Aspira dos veces a la semana, como mínimo, si tienes alfombras. Para otros suelos como parquet o terrazo, bastará con pasar la mopa humedecida para que los pelos queden atrapados y no vuelen. La aspiradora es un buen  aliado para las zonas de difícil acceso.

                            Existe una gran cantidad de utensilios que nos ayudan con la eliminación de pelos en el hogar. Escobas de goma, mopas atrapapolvo, guantes de microfibra o cepillos de terciopelo son algumos de ellos.</p>
						</div>
