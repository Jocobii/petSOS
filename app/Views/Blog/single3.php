							<a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">Cómo entrenar a tu cachorro en el departamento</a>
							<p class="card-text">Tener a una mascota genera muchas alegrías, como jugar en el parque, acariciarlos en el sofá, hacer que jueguen con una pelota… almohadas destrozadas, “sorpresas” en el departamento. Tener a un canino en el departamento trae ventajas y desventajas, sin importar si la nueva mascota es uno muy pequeñito o una gran bestia. Por eso, deberás seguir los siguientes pasos que el Grupo Inmobiliario Ciudaris para que el animalito crezca feliz y saludable.
                            <br>
                            <br>
                            <a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">Departamento a prueba de cachorros</a>
                           Haz que el cachorro entienda desde el principio haciendo del departamento un lugar amigable con los animales. Retira cables y cordones fuera de la vista de todos para que ciertas bocas curiosas no se las devoren. Asimismo, habrán narices curiosas que querrán explorar nuevos aromas, así que mantén las puertas de los gabinetes cerradas para que el perrito no encuentre los desinfectantes y aromatizantes. Asegúrate también de mantener el basurero bien tapado.

                            Guarda toda la ropa y zapatos dentro del armario, porque es probable que el perro juegue con cosas con el aroma de sus dueños, lo que convierte a tu polo y zapatillas favoritas en blancos perfectos para el canino.
                            <br>
                            <br>
                           ¿No estás seguro de qué más debes de proteger? Siéntate en el piso y mire el mundo a tu alrededor con la mentalidad de un perrito que explora un nuevo lugar. ¿Algo se ve delicioso o masticable? Pues asegúrate que eso no esté a su alcance
                            <a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">Prepara su cama y empieza a entrenarlo</a>
                            Puede sonar tentador dormir con el cachorro durante las primeras noches que esté en casa, pero si quieres que el perro duerma todas las noches en su propia cama, es importante que empieces a acostumbrarlo desde el primer día.

                           Prepara una caja con una cama cómoda y suave para el perrito y colócala en tu habitación. Es posible que durante las primeras noches te desobedezca y no se quede en su cama, pero con el paso del tiempo se acostumbrará a la caja y la verá como un lugar seguro.</p>
                            <br>
                            <br>
                            <a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">La hora de las “necesidades”</a>
                            
                            <p>Un cachorro necesita pasar mucho tiempo afuera del departamento. Trata de sacar al perrito una vez cada hora para evitar que este ensucie la alfombra u otras partes del apartamento. Asegúrate de estar alejado de las áreas compartidas y recoge lo que haga el perro, pues los vecinos te lo agradecerán.
                            
                            <a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">Haz uso de la zona para mascotas</a><br>
                            Muchas veces, la vida en un departamento significa olvidarse de tener un patio y jardín propios. Sin embargo, si vives en un ambiente amigable con las mascotas, entonces lo más probable es que haya un área especial para los perros. Lleva al tuyo a la zona para mascotas y deja que explore y que juegue con otros perritos de la comunidad. Es una gran manera de dejar a tu perro liberar toda su energía afuera del departamento.</p>
