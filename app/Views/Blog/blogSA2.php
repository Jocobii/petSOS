							<a href="single.html" class="text-bl font-weight-bold blog-grid-title mt-4 mb-3">Consejos para el baño de tu mascota</a>
							<p class="card-text">El aseo de nuestra mascota se basará en saber cuándo y cómo lo cepillamos, si es aconsejable o no bañarlo y si hacen falta otros productos como son las soluciones o las espumas.
                            <br>
                            <br>

                            En general, es bueno cepillar a nuestra mascota por el simple hecho que genera un vínculo entre ella y su dueño; también conseguimos con ello, que el animal se habitúe a que lo manipulen y a liberar el pelo muerto que queda en la piel y que provoca nudos. En animales de pelo corto podemos utilizar un paño húmedo.
                            <br>
                            <br>
                            Para determinar con qué frecuencia y con qué producto debemos bañar  a nuestro perro o gato hay que tener en cuenta diferentes aspectos de nuestras mascotas, como son:</p>
                            <br>
                            <br>
                            <p>Edad: cuando tenemos un cachorro de muy corta edad que todavía no está totalmente vacunado muchas veces usamos espumas secas para no tenerlo que mojar con el riesgo de que se resfríe. Una vez pasada esta época, la frecuencia del baño depende de la raza, el tipo de pelo y la convivencia que tengamos con él; es muy distinto un perro que vive todo el año en el jardín que un perro que está encima nuestro todo el día.</p>
						</div>
					</div>
					
					<p>Pelo: hay que diferenciar entre pelo liso, medio o duro y entre pelo corto, medio y largo. Cada gama de champús tiene el específico para cada tipo de pelo.
                   <br>
                   <br>
                    Piel: el pH de la piel canina es alrededor de 7, es decir es un pH neutro, distinto del pH ácido de 5,5 a 7 de los humanos, por eso no hay que utilizar nunca los champús nuestros para bañar a la mascota por muy bueno que sea.
                    <br>
                    <br>
                    Color del manto: la melanina es uno de los pigmentos que se encuentran en la piel y es el determinante del color del manto del perro. Los colores claros son más sensibles y más sucios por lo que se deberán limpiar con mayor frecuencia pero con un champú especial para ellos. Los animales albinos carecen de melanina. </p>
					
					<p class="my-3">Problemas o enfermedades de la piel: si el animal padece alguna enfermedad habrá que medicar esa piel con un champú especial. Muchas veces se realizan baños frecuentes, incluso a veces diarios, ya que son champús que prescribe el veterinario como tratamiento; normalmente estos productos no hacen demasiada espuma y hay que dejarlos que actúen un cierto tiempo antes de aclararlo.</p>
					
					
					
